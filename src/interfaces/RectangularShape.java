package interfaces;

public class RectangularShape implements Shape {
    private int width;
    private int height;

    public RectangularShape(int width, int height) {
        this.width = width;
        this.height = height;
    }
    public boolean contains(int x, int y){
        return true;
    }
    public double getArea(){
        return getArea();
    }
}
