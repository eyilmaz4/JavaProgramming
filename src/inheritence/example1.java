package inheritence;

public class example1 {
    private static int additionPrivateStatic(int a, int b){
        return a+b;
    }
    public static int additionPublicStatic(int a, int b){
        return a+b;
    }

    private int additionPrivate(int a, int b){
        return a+b;
    }
    public int additionPublic(int a, int b){
        return a+b;
    }
}
